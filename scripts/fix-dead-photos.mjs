// Audits images.js: any Premier League photo URL that 403/404s at both sizes
// gets re-resolved via Wikipedia (same lookup as fetch-images.mjs).
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const images = new Function(
  `const window={}; ${readFileSync(path.join(root, "images.js"), "utf8")}; return window.PLAYER_IMAGES;`
)();

const UA = { "User-Agent": "lineups-app/1.0 (personal project)" };
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function ok(url) {
  const res = await fetch(url, { method: "HEAD", headers: UA });
  return res.ok;
}

async function wikiThumb(title) {
  const res = await fetch(
    `https://en.wikipedia.org/api/rest_v1/page/summary/${encodeURIComponent(title.replace(/ /g, "_"))}`,
    { headers: UA }
  );
  if (!res.ok) return null;
  const d = await res.json();
  if (d.type === "disambiguation") return null;
  if (!/foot|goalkeeper|soccer/.test(`${d.description ?? ""} ${d.extract ?? ""}`.toLowerCase())) return null;
  return d.thumbnail?.source ?? null;
}

const WIKI_OVERRIDES = {
  Estêvão: ["Estêvão Willian"],
  "Alisson Becker": ["Alisson Becker", "Alisson"],
  Rodri: ["Rodri (footballer, born 1996)"],
  "João Pedro": ["João Pedro (footballer, born September 2001)"],
  "Reece James": ["Reece James (footballer, born 1999)"],
};

let fixed = 0, dropped = 0;
for (const [name, url] of Object.entries(images)) {
  if (!url.includes("resources.premierleague.com")) continue;
  if (await ok(url)) continue;
  const alt = url.replace("/250x250/", "/110x140/");
  if (await ok(alt)) {
    images[name] = alt;
    fixed++;
    console.log(`resized: ${name}`);
    continue;
  }
  await sleep(600);
  let wiki = null;
  for (const t of [...(WIKI_OVERRIDES[name] ?? []), name, `${name} (footballer)`]) {
    wiki = await wikiThumb(t);
    if (wiki) break;
    await sleep(600);
  }
  if (wiki) {
    images[name] = wiki;
    fixed++;
    console.log(`wiki: ${name}`);
  } else {
    delete images[name];
    dropped++;
    console.log(`DROPPED (initials avatar): ${name}`);
  }
  await sleep(300);
}

writeFileSync(
  path.join(root, "images.js"),
  `// Generated by scripts/fetch-images.mjs — do not edit by hand.\nwindow.PLAYER_IMAGES = ${JSON.stringify(images, null, 2)};\n`
);
console.log(`\nDone: ${fixed} fixed, ${dropped} dropped`);
